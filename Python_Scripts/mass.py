class SignalAnalysis:
    def __init__(self, Data, QueryDate, RegIndex=None, windowSize=5, lookback=True, threshold = 0.90):
        self.data = Data
        self.ret = np.log(self.data)-np.log(self.data.shift())
        self.ref = QueryDate
        self.index = RegIndex
        self.win = windowSize
        self.look = lookback
        self.thd = threshold
        self.em20 = self.data.ewm(span = 20, adjust=False).mean()[20:]
        self.em200 = self.data.ewm(span = 200, adjust=False).mean()[200:]
        self.emt = (self.em20-self.em200)[200-20:]
        self.vol = self.ret[1:].rolling(65).std(ddof=1)[65:]
        self.mass = Match(self.data, self.ref, windowSize=self.win, lookback=self.look , threshold = self.thd)
        self.ext = None
    
    def getEMTable(self):
        reg = self.mass.getRegime().iloc[self.index]
        sd= reg['start'].values
        ed= reg['end'].values
        reg['startEM']=self.emt.loc[sd].values
        reg['endEM']=self.emt.loc[ed].values
        reg['changeEM']= (reg.endEM - reg.startEM)/ np.abs(reg.startEM)
        reg['avgEM']= [self.emt.loc[sd[i]:ed[i]].values.mean() for i in range(reg.shape[0])]
        reg['RevertingEM']= [0 if (reg.endEM.iloc[i]-reg.avgEM.iloc[i])/(reg.avgEM.iloc[i]-reg.startEM.iloc[i])>0 else 1 for i in range(reg.shape[0])]
        return reg

    def getVolTable(self):
        reg = self.mass.getRegime().iloc[self.index]
        sd= reg['start'].values
        ed= reg['end'].values
        reg['startVol']=self.vol.loc[sd].values
        reg['endVol']=self.vol.loc[ed].values
        reg['changeVol']= (reg.endVol - reg.startVol)/ np.abs(reg.startVol)
        reg['avgVol']= [self.vol.loc[sd[i]:ed[i]].values.mean() for i in range(reg.shape[0])]
        reg['RevertingVol']= [0 if (reg.endVol.iloc[i]-reg.avgVol.iloc[i])/(reg.avgVol.iloc[i]-reg.startVol.iloc[i])>0 else 1 for i in range(reg.shape[0])]
        return reg

    def getGenTable(self, ExternalData):
        self.ext = ExternalData
        reg = self.mass.getRegime().iloc[self.index]
        sd= reg['start'].values
        ed= reg['end'].values
        reg['startExt']=self.ext.loc[sd].values
        reg['endExt']=self.ext.loc[ed].values
        reg['changeExt']= (reg.endExt - reg.startExt)/ np.abs(reg.startExt)
        reg['avgExt']= [self.ext.loc[sd[i]:ed[i]].values.mean() for i in range(reg.shape[0])]
        reg['RevertingExt']= [0 if (reg.endExt.iloc[i]-reg.avgExt.iloc[i])/(reg.avgExt.iloc[i]-reg.startExt.iloc[i])>0 else 1 for i in range(reg.shape[0])]
        return reg
    
    def getCombined(self):
        df1 = self.getEMTable()
        df2 = self.getVolTable()
        return pd.merge(df1, df2, on=['idx', 'start', 'end', 'dp'], how='outer')
    
    def combineExt(self, df, df_ext):
        return pd.merge(df, df_ext, on=['idx', 'start', 'end', 'dp'], how='outer')



