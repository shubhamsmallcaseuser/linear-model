class Match:
    def __init__(self, Data, QueryDate, windowSize=5, lookback=True, threshold = 0.90):
        self.data = Data.loc[:QueryDate]
        self.ref = QueryDate
        self.win = windowSize
        self.look = lookback
        self.hist = self.data.loc[:self.ref]
        if self.look is False:
            self.query = self.data.loc[self.ref:][:self.win]
        else:
            self.query = self.hist[-self.win:]
        self.num=None
        self.thd = threshold
        self.thdcorr = np.sqrt(2*(self.win - (self.win-1)* self.thd))

    def getDP(self, normalize=True):
        if normalize is True:
            dp = MASS(Query=self.query, Target=self.hist).getNormalised_updated()
        else:
            dp = MASS(Query=self.query, Target=self.hist).getActual()
        return dp
    
    def getDates(self):
        dp = self.getDP()
        dp['DDiff'] = pd.to_datetime(self.ref) -dp.index
        dp.sort_values("Distance", inplace= True)
        return dp[(dp['Distance'] < self.thdcorr) & (dp['Distance'] > 1.414214) & (dp['DDiff'] > pd.Timedelta(25, "d"))]
    
