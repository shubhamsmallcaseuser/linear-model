class MMCompSelection:

    def __init__(self, Data, iteration=100, maxComp=10, state=0):
        self.data = Data
        self.iter = iteration
        self.state =state
        self.comp = maxComp

    def getIC(self):
        aic=[]
        bic=[]
        # GridSearch
        for i in range(1, self.comp):
            gmm = GMM(self.data, component=i, iteration=self.iter, state=self.state)
            # AIC
            aic.append(gmm.gmm.aic(gmm.data))
            # BIC
            bic.append(gmm.gmm.bic(gmm.data))
        # Dataframe
        df = pd.DataFrame(aic, columns=['AIC'])
        df['BIC'] = bic
        df['index'] = np.arange(1, self.comp)
        return df.set_index('index')

    def getBestComp(self):
        df = self.getIC()
        # getting
        bestA=df.loc[:, 'AIC'].idxmin()
        bestB=df.loc[:, 'BIC'].idxmin()
        return bestA, bestB

    def plotIC(self, Axes=None):
        # getting values
        ic = self.getIC()
        #setting up the axes
        if (Axes is None):
            plt.figure(figsize=(12,6))
            ax = plt.gca()
        else:
            # given axes
            ax=Axes

        ic.plot(ax=ax)

class EM:

    def __init__(self, Data, InitMean, InitStd, InitProb=None, components=None, maxIteration=1000, tolerance = 1e-5):
        # converting into 1-D data
        self.data = np.asarray(Data).flatten()
        # n
        self.len = self.data.shape[0]
        self.maxiter = maxIteration
        # m
        self.comp = components
        if self.comp is None:
            self.comp = InitMean.shape[0]
        self.tol = tolerance
        # random guess for initial mean and std
        self.mean0 = InitMean
        self.std0 =  InitStd
        if (InitProb is None):
            # equal probability 
            self.prob0 = np.array([1/self.comp]*self.comp)
        else:
            self.prob0 = InitProb

        # distribution object
        self.dist = [NormalDist(self.data, mu=self.mean0[i], sigma=self.std0[i]) for i in range(self.comp) ]

        self.weight = np.array(self.prob0) 
        self.lastLL = np.full((self.maxiter, ), np.nan) 
        self.resp = np.empty((self.len, self.comp)) 
        self.logD = np.empty((self.len, self.comp))
        
    def runAlgorithm(self):

        distributions= self.dist
        weight = self.weight 
        last_ll = self.lastLL
        resp = self.resp
        log_density = self.logD
        
        # init counter 
        iteration = 0 
        while True:

            # E-step 
            # compute responsibilities aka weights
            for d in range(self.comp):
                log_density[:, d] = distributions[d].getLogDensity()
                
            # normalize responsibilities of distributions so they sum up to one for example
            resp = weight[np.newaxis, :] * np.exp(log_density)
            resp /= np.sum(resp, axis=1)[:, np.newaxis]
            
            # compute log-likelihood
            # matrix multiplication
            log_likelihood = np.sum(resp @ log_density.T) 
            #log_density, resp, log_likelihood = self.ExpectationStep()
            
            # M-step 
            for d in range(self.comp):
                distributions[d].estimateParameters(resp[:, d])

            weight = np.mean(resp, axis=0)
            #weight = self.MaximisationStep()
            
            # check convergence
            if np.isnan(log_likelihood):
                last_ll[0] = log_likelihood
                print('loglk is nan')
                break
            
            if  ((last_ll[0] - log_likelihood) / last_ll[0]) <= self.tol:
                last_ll[0] = log_likelihood
                print('change in loglk less than tolerance')
                break

            if iteration >= self.maxiter:
                last_ll[0] = log_likelihood
                print('reached maximum iterations')
                break

            # store value of current iteration in last_ll[0]
            #   and shift older values to the right   
            last_ll[1:] = last_ll[:-1]
            last_ll[0] = log_likelihood

            # self.logD = log_density
            # self.weight = weight
            # self.resp = resp
            
            # display     
            mus = np.array([distributions[i].mean for i in range(self.comp)])
            sigs = np.array([distributions[i].std for i in range(self.comp)])
            
            
            self.iterData = (pd.DataFrame(np.vstack([mus, sigs, weight]),
                                    index=['means', 'sigmas', 'weights']).round(4))
            
            if iteration % 50==0:
                print(f'iteration: {iteration}')
                print(f"ll new: {last_ll[0].round(4)}")
                print(self.iterData.T)
            iteration += 1  

    def ExpectationStep(self):
        distributions= self.dist
        logD = self.logD
        weight = self.weight

        # compute responsibilities aka weights
        for d in range(self.comp):
            logD[:, d] = distributions[d].getLogDensity()
            
        # normalize responsibilities of distributions so they sum up to one for example
        resp = weight[np.newaxis, :] * np.exp(logD)
        resp /= np.sum(resp, axis=1)[:, np.newaxis]
        
        # compute log-likelihood
        # matrix multiplication
        logL = np.sum(resp @ logD.T) 

        return logD, resp, logL

    def MaximisationStep(self):
        distributions= self.dist 
        resp= self.resp      
        # now that we have the new weights we update the parameters
        # of the distributions
        for d in range(self.comp):
            distributions[d].estimateParameters(resp[:, d])

        weight = np.mean(resp, axis=0)
        return weight

