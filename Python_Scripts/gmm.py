class Prediction:
    def __init__(self, Data, Mean, freq=22, length=150):
        self.data = Data
        self.mean = np.exp(Mean) - 1
        self.freq = freq
        self.len = length

    def getLevels(self):
        # initiating lists to store data        
        Price=[]
        index=[]
        level=[self.data.iloc[0]]
        # calculating the price levels 
        for i in range(int(self.len/self.freq + 1)):
            Price.append(self.data.iloc[self.freq*i].values)
            index.append(self.data.iloc[self.freq*i].name)
        # level calculation
        for i in range(1, int(self.len/self.freq + 1)):
            level.append(self.data.iloc[self.freq*(i-1)].values*(self.mean+1)**self.freq)
        # Dataframe 
        Pred= pd.DataFrame(Price, columns=['Actual'])
        Pred['Date']=index
        Pred['Levels'] = level
        Pred['Levels']=Pred.Levels.apply(lambda x: x[0])
        Pred.loc[0,'Levels'] = np.nan
        Pred=Pred.set_index('Date')
        Pred['Residuals'] = (Pred.Actual-Pred.Levels)#/self.freq
        Pred.iloc[-1, [0, 2]] =np.nan
        return Pred

    def plotLevels(self, Axes=None, showActual=True):
        pred = self.getLevels()
        if (Axes==None):
            plt.figure(figsize=(18, 6))
            ax = plt.gca()
            ax.set(title='Price Level- Actual v/s Prediction')
        else:
            ax=Axes
        
        pred.Levels.plot(ax=ax, marker='o', color='b', label='Predcition')
        if (showActual):
            pred.Actual.plot(ax=ax, marker='o', color='r', label='Actual')
        ax.legend()