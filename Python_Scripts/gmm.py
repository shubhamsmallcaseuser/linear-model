class EstimateParameters:

    def __init__(self, Data, Weights):
        # expected 1-D data
        self.data = np.asarray(Data)
        self.weight = np.asarray(Weights)
        # sum of the weights
        self.wsum = np.sum(self.weight)

    def getWeightedMean(self):
        # weighted mean
        Wmean = np.sum(self.weight * self.data) / self.wsum
        return Wmean

    def getWeightedSD(self):
        mean=self.getWeightedMean()
        # weighted variance
        Wvar = np.sum(self.weight * (self.data - mean)**2 ) / self.wsum
        # volatility
        Wstd = np.sqrt(Wvar)
        return Wstd

class NormalDist:

    def __init__(self, Data, mu=None, sigma=None):
        # 1-D data
        self.data = Data
        self.mean= mu
        self.std = sigma
        self.len = self.data.shape[0]

        if self.mean is None:
            # if mean of regime is not given
            self.mean = self.data.mean()
         
        if self.std is None:
            # if std of regime is not given
            self.std = self.data.std()

        # getting the range
        self.min = self.data.min()
        self.max = self.data.max()

    def genNormPDF(self):
        # define the datapoints
        x=np.linspace(self.min, self.max, self.len)
        # gaussian probability distribution 
        y = stats.norm.pdf(x, self.mean, self.std)
        
        df = pd.DataFrame(y, columns=['PDF'])
        df['x'] = x
        return df.set_index('x')

    def genNormCDF(self):
        # define the datapoints
        x=np.linspace(self.min, self.max, self.len)
        # gaussian cumulative distribution 
        y = stats.norm.cdf(x, self.mean, self.std)
        df = pd.DataFrame(y, columns=['CDF'])
        df['x'] = x
        return df.set_index('x')

    def getDensity(self):
        # probability distribution function
        pdf = stats.norm.pdf(self.data, loc=self.mean, scale=self.std)
        return pdf

    def getLogDensity(self):
        # log probability distribution function
        log_pdf = stats.norm.logpdf(self.data, loc = self.mean, scale =self.std)
        return log_pdf

    def estimateParameters(self, weights):
        esp = EstimateParameters(self.data, weights)
        self.mean= esp.getWeightedMean()
        self.std = esp.getWeightedSD()

