class ModelSelection:
    def __init__(self, Data, pMax=5, qMax=5):
        self.data = Data
        self.p = pMax
        self.q = qMax

    def getICseries(self):
        aicc=[]
        for i in range(self.p):
            for j in range(self.q):
                try:
                    aic = ARIMA(self.data, order=(i, 0, j)).fit().aic
                    bic = ARIMA(self.data, order=(i, 0, j)).fit().bic
                    aicc.append([i, j, aic, bic])
                except:
                    continue
        df = pd.DataFrame(aicc, columns=['p', 'q', 'AIC', 'BIC'])
        return df

