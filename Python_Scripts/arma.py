class Model:
    def __init__(self, Data, method='CE', p=None, d=0, q=None, trend='ct'):
        self.data = Data
        self.p = p
        # d must be zero for every modelling
        self.d = d
        self.q = q
        self.trend = trend
        if self.p is None and self.q is None:
            raise ValueError("Specify p or q, both can't be None")
        self.method= method

        if self.d is not 0:
            self.data = self.data.diff()[1:]
            print('ARIMA d={}'.format(self.d))
    
    def getParams(self):
        method = self.method
        if self.q is None:
            if method is 'YW':
                mod = AR(self.data, p=self.p, method ='YW', trend=self.trend)
                params = mod.getParams()
            elif method is 'Burg':
                mod = AR(self.data, p=self.p, method ='Burg', trend=self.trend)
                params = mod.getParams()
            else:
                mod = AR(self.data, p=self.p, method ='CE', trend=self.trend)
                params = mod.getParams()

        elif self.p is None:
            if method is 'Innovation':
                mod = MA(self.data, q=self.q, method ='Innovation', trend=self.trend)
                params = mod.getParams()
            else:
                mod = MA(self.data, q=self.q, method ='CE', trend=self.trend)
                params = mod.getParams()
        
        else:
            try:
                mod = ARMA(self.data, p=self.p, q=self.q, method ='HR', trend=self.trend)
                params = mod.getParams()
            except:
                mod = ARMA(self.data, p=self.p, q=self.q, method ='CE', trend=self.trend)
                params = mod.getParams()
        self.model = mod.model
        return params

