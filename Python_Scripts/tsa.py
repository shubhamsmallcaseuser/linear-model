class Smooth:

    def __init__(self, Data, lookback=20):
        self.data = Data
        self.span = lookback

    def getRollWMA(self, window='hanning'):
        # Localise Variable
        data = self.data
        # Rolling window Mean
        winAvg = data.rolling(self.span, min_periods=self.span, center=True, win_type=window).mean()
        return winAvg

    def getSMA(self):
        # Loacalise Variable
        data = self.data
        # Simple Moving Average
        SMA = data.rolling(self.span, min_periods=self.span).mean()
        return SMA

    def getEMA(self):
        # Localise Variable
        data = self.data       
        # Exponential Moving averages
        EMA = data.ewm(span = self.span, adjust=False).mean()
        return EMA

class Test:

    def __init__(self, Data, confidence=0.95):
        self.data = Data.values.flatten()
        self.alpha = 1- confidence

    def DickeyFuller(self):
        stats = adfuller(self.data)
        return stats[:2]

    def JarqueBera(self):
        stats = st.jarque_bera(self.data)
        return stats

    def LjungBox(self, lags=None):
        stats = acorr_ljungbox(self.data, lags = lags, return_df=True)
        stats['SAC'] = stats['lb_pvalue'] < self.alpha
        return stats

    def checkStationarity(self):
        stats = self.DickeyFuller()
        return stats[1] < self.alpha

    def checkNormality(self):
        stats = self.JarqueBera()
        return stats[1] > self.alpha

