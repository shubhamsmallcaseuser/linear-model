class FractionalDifferencing:

    def __init__(self, numLag=14, fraction=0.5, tolerance=1e-4):
        self.lag = numLag
        self.frac = fraction
        self.tol = tolerance

    def convergence(self, arr):
        for i in range(1, len(arr)-1):
            tol = (arr[i+1]-arr[i])
            if np.abs(tol) <= self.tol:
                # print('convergence')
                break       
        return i+1, arr[i]

    def getWeights(self):
        # initialise the weight 
        w=[1]
        wdiff = []
        # assigning weights
        for k in range(1, self.lag+2):
            # calculate weights using binamial theorem
            weight = -w[k-1]*((self.frac-k+1))/k
            # append to the list of weights to use it further
            w.append(weight)
        # reshape the weight matrix
        idx = self.convergence(w)[0]
        if self.lag > idx : 
            self.lag = idx
        w = np.asarray(w).reshape(-1,1) 
        return w[: self.lag]

    def getDifferencedSeries(self, Data):
        # getting values
        w = self.getWeights()
        # initialise results
        x=Data.values
        series =[]
        for i in range(self.lag, len(x)):
            ser=0
            for k in range(self.lag):
                ser+= x[i-k]*w[k]
            series.append(ser[0])
        df = pd.DataFrame(series)
        return df

class FractionEstimate:
    def __init__(self, Data, numLag= 14, interval=0.1, confidence=0.95, tolerance=1e-2):
        self.data = Data
        self.lag = numLag 
        # array of the fractions
        self.frac = np.arange(1, 0, -interval)
        self.alpha = confidence
        self.tol = tolerance

    def getEstimate(self):
        # initialise a list
        fractions = []
        for i, frac in enumerate(self.frac):
            # temp array for fractional differencing series
            temp = FractionalDifferencing(self.lag, fraction=frac, tolerance=self.tol).getDifferencedSeries(self.data)
            # testing for stationary
            test = Test(temp, confidence=self.alpha).checkStationarity()
            fractions.append([i, round(frac, 2),  test])
        # store in dataframe
        df = pd.DataFrame(fractions, columns=['Index', 'Fraction', 'Stationary'])
        return df.set_index('Index')

    def getBest(self):
        # getting smallest fraction for which it can be stationary
        x = self.getEstimate()
        if x['Stationary'].sum() !=0:
            f = x[x.loc[:, 'Stationary'] == True].iloc[-1, 0]
        else: 
            f = 1
        return f

class BackOperation:
    def __init__(self, Actual, DiffSeries, numLag=14, fraction=0.5, tolerance=1e-4):
        self.data = DiffSeries
        self.act = np.log(Actual)
        self.lag = numLag
        self.init = self.act[:self.lag]
        
        self.len = len(self.data)
        self.frac = fraction
        self.tol = tolerance
        self.weights = FractionalDifferencing(self.lag, fraction=self.frac, tolerance = self.tol).getWeights()[1:]
        
    def getPrediction(self):
        # Need the array uptill 2*lag
        prices = list(self.init)
        # starting from self.lag:2*self.lag and so on to predict 28th observation
        for i in range(1, self.len+1):
            price = self.data[i-1]
            price -= np.sum(prices[i : i + self.lag-1] * self.weights.flatten()[::-1])
            prices.append(price)
        return pd.DataFrame(np.exp(prices))
    
    def plot(self, Axes=None):
        price = self.getPrediction()
        if Axes is None:
            plt.figure(figsize=(24,8))
            ax = plt.gca(title='Actual vs Predicted Price')
