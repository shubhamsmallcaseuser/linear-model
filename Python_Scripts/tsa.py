class SizeEqualiser:

    def __init__(self, Small, Large):
        self.large = Large
        self.small = Small
        self.lsize = len(self.large)
        self.ssize = len(self.small)
        
        if (self.ssize > self.lsize):
            raise ValueError("Reverse Input order")
    
    def checkSize(self):
        if (self.lsize == self.ssize):
            # same size
            print('Sizes are same!!')
        
        return self.lsize == self.ssize

    def matchSize(self):
        # Loacalize Variable
        large = self.large
        small = self.small

        if self.checkSize() is False:
            # In case of different size
            temp = np.zeros(len(large)-len(small))
            small = np.hstack((small, temp))

        return small, large

class ImpulseResponse:
    
    def __init__(self, ResComp, FuncComp, timeStep=1, impLen = None):
        # Equalise size of array
        if (len(ResComp)> len(FuncComp)):
            self.phi, self.theta = SizeEqualiser(FuncComp, ResComp).matchSize()
        elif (len(ResComp)< len(FuncComp)):
            self.theta, self.phi = SizeEqualiser(ResComp, FuncComp).matchSize()
        else:
            self.theta = ResComp
            self.phi = FuncComp

        self.len = impLen
        self.t = timeStep

    def getSystemInstance(self):
        # Localise Variable
        Res = self.theta
        Func = self.phi

        # (Num, Den, steps)
        SysIns = Res, Func, self.t
        return SysIns
 
    def getImpRes(self): 
        # get system instance
        SysIns = self.getSystemInstance()
        len = self.len
        # Conversion into impulse response
        time, impulse = dimpulse(SysIns, n=len)
        # result array
        impulse = impulse[0].flatten()

        ImpResponse = pd.Series(impulse, index = time, name = 'Impulse Response')

        return ImpResponse

    def plot(self, Axes=None):
        # getting values
        ImpResponse = self.getImpRes()

        if (Axes==None):
            # Setting up the Axes
            plt.figure(figsize=(12,6))
            ax = plt.gca()
            ax.set(title='Impulse response', xlabel='time', ylabel='response')
        else:
            # Get the Axes
            ax = Axes
        # plots stem chart
        ax.stem(ImpResponse)

